type TButtonProps = {
  type?: 'button' | 'submit' | 'reset';
  theme?: keyof typeof buttonThemeClassNames;
  children?: ReactNode;
  onClick?: () => void;
  className?: string;
  style?: CSSProperties;
};

const Button = ({
  theme,
  className,
  children,
  type,
  style,
  onClick,
}: TButtonProps) => {
  const classNames = `cursor-pointer ${
    buttonThemeClassNames[theme ?? 'default']
  } ${className}`;

  if (type === 'submit')
    return <input type="submit" className={classNames} style={style} />;
  else if (type === 'reset')
    return <input type="reset" className={classNames} style={style} />;

  return (
    <button
      type="button"
      onClick={onClick}
      className={classNames}
      style={style}
    >
      {children}
    </button>
  );
};

export default Button;
